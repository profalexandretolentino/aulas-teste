// Declaração da variável para armazenar a soma
let soma = 0;

// Utiliza um laço for para iterar de 1 a 10
for (let i = 1; i <= 10; i++) {
    soma += i; // Adiciona o valor de i à soma
    //console.log("A soma dos números de 1 a 10 é: " + soma);
}

// Exibe a soma final no console
console.log("A soma dos números de 1 a 10 é: " + soma);
