// Criando um vetor com 10 números
let vetor = [120, 7, 15, 22, 34, 51, 6, 11, 10, 9];

// Laço de repetição para percorrer o vetor
for (let i = 0; i < vetor.length; i++) {
    // Verificando se o número é par
    if (vetor[i] % 2 === 0) {
        console.log(vetor[i]); // Imprime o número se for par
    }
}